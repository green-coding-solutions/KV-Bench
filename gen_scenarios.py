#!/usr/bin/env python3
"""
gen_scenarios.py — Generate the GMT usage scenarios for KV-Bench.

The whole repo is a *fair* comparison: every store must run the identical
benchmark flow, differing only in connection details and (for the T1 tier) its
own engine configuration. Six stores x two drivers x two tuning tiers is far too
much near-identical YAML to keep in sync by hand, so the scenario files are
generated from the tables below. The generated files are committed; regenerate
and commit whenever this script changes:

    ./gen_scenarios.py           # regenerate benchmarks/*/*.yml + compose copies
    ./gen_scenarios.py --check   # fail if the committed files are stale (CI)

check_repo.py still independently validates the result (compose copies identical,
equal resource budgets, identical fairness knobs), so a hand-edit that dodges the
generator is still caught.
"""

from __future__ import annotations

import argparse
import re
import sys
from pathlib import Path

REPO = Path(__file__).resolve().parent

# ---------------------------------------------------------------------------
# Store table. `binding` is the YCSB binding name; the RESP stores all share the
# `redis` binding. `keep` is the compose service kept in the scenario (the store
# under test); every other store service + the other driver are removed.
# ---------------------------------------------------------------------------
STORES = {
    "redis":     {"name": "Redis",     "svc": "redis",     "binding": "redis",        "resp": True},
    "valkey":    {"name": "Valkey",    "svc": "valkey",    "binding": "redis",        "resp": True},
    "keydb":     {"name": "KeyDB",     "svc": "keydb",     "binding": "redis",        "resp": True},
    "dragonfly": {"name": "Dragonfly", "svc": "dragonfly", "binding": "redis",        "resp": True},
    "memcached": {"name": "Memcached", "svc": "memcached", "binding": "memcached",    "resp": True},
    "pg":        {"name": "PostgreSQL","svc": "postgres",  "binding": "postgrenosql", "resp": False},
}

ALL_SVCS = ["redis", "valkey", "keydb", "dragonfly", "memcached", "postgres"]
DRIVERS = ["ycsb", "memtier"]

# YCSB core workloads run against a single load, in the canonical order
# (A,B,C,F,D share one load; D inserts last). E is scan-heavy and unsupported by
# the memcached binding, so it is intentionally excluded to keep every store's
# work identical — see TUNING.md.
# NB: the description becomes part of the flow-step `name`, which GMT restricts to
# ^[\.\s0-9a-zA-Z_\(\)-]+$ — so no '/', '%', ',' or '+' here.
YCSB_WORKLOADS = [
    ("a", "workloada", "read-update 50-50"),
    ("b", "workloadb", "read-heavy 95-5"),
    ("c", "workloadc", "read-only"),
    ("f", "workloadf", "read-modify-write"),
    ("d", "workloadd", "read-latest"),
]

# T1 (envelope-sized) engine configuration, merged over compose.yml's service.
# Only the store configuration changes between tiers; the flow never does. Sized
# to the fixed 4-CPU / 8-GB container; persistence/durability left at default so
# the T0->T1 delta is pure resource sizing. See TUNING.md.
T1_COMMAND = {
    "redis": [
        "redis-server", "--maxmemory", "6gb", "--maxmemory-policy", "allkeys-lru",
        "--io-threads", "4",
    ],
    "valkey": [
        "valkey-server", "--maxmemory", "6gb", "--maxmemory-policy", "allkeys-lru",
        "--io-threads", "4",
    ],
    "keydb": [
        "keydb-server", "--maxmemory", "6gb", "--maxmemory-policy", "allkeys-lru",
        "--server-threads", "4",
    ],
    "dragonfly": [
        "--maxmemory=6gb", "--proactor_threads=4", "--cache_mode=true",
    ],
    "memcached": [
        "memcached", "-m", "6144", "-t", "4",
    ],
    "pg": [
        "postgres",
        "-c", "shared_buffers=2GB",
        "-c", "effective_cache_size=6GB",
        "-c", "maintenance_work_mem=512MB",
        "-c", "work_mem=16MB",
        "-c", "wal_buffers=16MB",
        "-c", "min_wal_size=1GB",
        "-c", "max_wal_size=4GB",
        "-c", "checkpoint_completion_target=0.9",
        "-c", "random_page_cost=1.1",
        "-c", "effective_io_concurrency=200",
        "-c", "max_worker_processes=4",
        "-c", "max_parallel_workers=4",
    ],
}

AUTOGEN = "# GENERATED by gen_scenarios.py — do not edit by hand; edit the generator and rerun."


def other_services(keep: str, driver: str) -> list[str]:
    """Compose service keys to remove: every store except `keep`, plus the other driver."""
    removed = [s for s in ALL_SVCS if s != keep]
    removed += [d for d in DRIVERS if d != driver]
    return removed


def disabled_block(keep: str, driver: str, keep_driver_indent: str = "") -> str:
    lines = []
    for svc in other_services(keep, driver):
        lines.append(f"  {svc}: # unused — empty key removes the service")
    return "\n".join(lines)


def ycsb_awk() -> str:
    # Parse YCSB's [OVERALL] RunTime + Throughput and emit ops actually completed
    # (throughput x runtime) as the SCI functional unit, stamped now.
    return (
        r"""awk '/\[OVERALL\], RunTime\(ms\)/{rt=$NF} """
        r"""/\[OVERALL\], Throughput\(ops\/sec\)/{tp=$NF} """
        r"""END{if(tp!=""){ops=int(rt/1000*tp+0.5);"date +%s"|getline ts;"""
        r"""printf("%s000000 ycsb_ops=%s\n",ts,ops)}}'"""
    )


def gen_ycsb(key: str, tier: int) -> str:
    s = STORES[key]
    svc = s["svc"]
    binding = s["binding"]
    props = f"/tmp/repo/db/{key}/ycsb/{key}_ycsb.properties"
    awk = ycsb_awk()
    suffix = " (T1 envelope-sized)" if tier else ""
    out = []
    out.append("---")
    out.append(f"name: YCSB {s['name']} benchmark{suffix}")
    out.append("author: Didi Hoffmann <didi@green-coding.io)")
    if tier:
        out.append("description: >")
        out.append(f"  YCSB on {s['name']} as a key-value store, tuning tier T1")
        out.append("  (envelope-sized engine config for the 4-CPU / 8-GB container; durability")
        out.append("  left at default so the T0->T1 delta is pure resource sizing). See TUNING.md.")
    else:
        out.append(
            f"description: Benchmarks {s['name']} as a key-value store with YCSB "
            "(core workloads A,B,C,F,D)"
        )
    out.append("")
    out.append(AUTOGEN)
    out.append("")
    out.append("compose-file: !include compose.yml")
    out.append("")
    out.append("custom_metrics:")
    out.append("  ycsb_ops: # functional unit: YCSB operations completed across the measured run phases")
    out.append("    unit: YCSB operation")
    out.append("    sci: true")
    out.append("")
    out.append("services:")
    if tier:
        out.append(f"  {svc}:")
        out.append("    command:")
        for arg in T1_COMMAND[key]:
            out.append(f'      - "{arg}"')
    out.append(disabled_block(svc, "ycsb"))
    out.append("  ycsb: # load driver; wait for the store to be healthy first")
    out.append("    depends_on:")
    out.append(f"      {svc}:")
    out.append("        condition: service_healthy")
    out.append("")
    if tier:
        # Reuse the T0 flow verbatim: tiers differ only in engine config.
        out.append(f"# Same benchmark flow as the default (T0) scenario ({key}.yml).")
        out.append(f"flow: !include ['{key}.yml', 'flow']")
        out.append("")
        return "\n".join(out)

    out.append("flow:")
    if not s["resp"]:  # PostgreSQL needs the KV database + usertable first.
        out.append("  - name: Create YCSB database and table")
        out.append("    container: postgres_container")
        out.append("    commands:")
        out.append("      - type: console")
        out.append("        note: CREATE YCSB SCHEMA")
        out.append(
            "        command: "
            "psql \"postgresql://postgres:postgres@localhost:5432/postgres\" -tc "
            "\"SELECT 1 FROM pg_database WHERE datname='ycsb'\" | grep -q 1 || "
            "psql \"postgresql://postgres:postgres@localhost:5432/postgres\" -c "
            "\"CREATE DATABASE ycsb\"; "
            "psql \"postgresql://postgres:postgres@localhost:5432/ycsb\" -c "
            "\"CREATE TABLE IF NOT EXISTS usertable "
            "(YCSB_KEY VARCHAR(255) PRIMARY KEY, YCSB_VALUE JSONB);\""
        )
        out.append("        shell: sh")
        out.append("        log-stdout: true")
    # Load phase (not a functional unit — setup, like BenchBase --load).
    out.append("  - name: Load dataset")
    out.append("    container: ycsb_container")
    out.append("    commands:")
    out.append("      - type: console")
    out.append("        note: YCSB LOAD")
    out.append(
        f"        command: ./bin/ycsb load {binding} -P workloads/workloada -P {props} "
        "2>&1 | tee /dev/stderr | grep -q '\\[OVERALL\\]'"
    )
    out.append("        shell: sh")
    out.append("        log-stdout: true")
    for _, wl, desc in YCSB_WORKLOADS:
        letter = wl[-1].upper()
        out.append(f"  - name: Run workload {letter} ({desc})")
        out.append("    container: ycsb_container")
        out.append("    commands:")
        out.append("      - type: console")
        out.append(f"        note: YCSB RUN {letter}")
        out.append(
            f"        command: ./bin/ycsb run {binding} -P workloads/{wl} -P {props} "
            f"2>&1 | tee /dev/stderr | {awk}"
        )
        out.append("        shell: sh")
        out.append("        log-stdout: true")
        out.append("        read-sci-stdout: true")
    out.append("")
    return "\n".join(out)


def gen_memtier(key: str, tier: int) -> str:
    s = STORES[key]
    svc = s["svc"]
    env = f"/tmp/repo/db/{key}/memtier/{key}_memtier.env"
    suffix = " (T1 envelope-sized)" if tier else ""
    out = []
    out.append("---")
    out.append(f"name: memtier {s['name']} benchmark{suffix}")
    out.append("author: Didi Hoffmann <didi@green-coding.io)")
    if tier:
        out.append("description: >")
        out.append(f"  memtier_benchmark on {s['name']}, tuning tier T1 (envelope-sized engine")
        out.append("  config for the 4-CPU / 8-GB container). See TUNING.md.")
    else:
        out.append(
            f"description: Benchmarks {s['name']} with memtier_benchmark "
            "(GET/SET throughput + latency)"
        )
    out.append("")
    out.append(AUTOGEN)
    out.append("")
    out.append("compose-file: !include compose.yml")
    out.append("")
    out.append("custom_metrics:")
    out.append("  memtier_ops: # functional unit: memtier operations completed in the measured run")
    out.append("    unit: memtier operation")
    out.append("    sci: true")
    out.append("")
    out.append("services:")
    if tier:
        out.append(f"  {svc}:")
        out.append("    command:")
        for arg in T1_COMMAND[key]:
            out.append(f'      - "{arg}"')
    out.append(disabled_block(svc, "memtier"))
    out.append("  memtier: # load driver; wait for the store to be healthy first")
    out.append("    depends_on:")
    out.append(f"      {svc}:")
    out.append("        condition: service_healthy")
    out.append("")
    if tier:
        out.append(f"# Same benchmark flow as the default (T0) scenario ({key}.yml).")
        out.append(f"flow: !include ['{key}.yml', 'flow']")
        out.append("")
        return "\n".join(out)

    # Totals row: memtier prints "Totals <ops/sec> ..."; ops completed = ops/sec x test-time.
    awk = (
        r"""awk '/^Totals/{o=$2} END{if(o!=""){ops=int(o*"""
        r"""'"$MEMTIER_TEST_TIME"'"""
        r"""+0.5);"date +%s"|getline ts;printf("%s000000 memtier_ops=%s\n",ts,ops)}}'"""
    )
    common = (
        "memtier_benchmark --host=$MEMTIER_HOST --port=$MEMTIER_PORT "
        "--protocol=$MEMTIER_PROTOCOL -c $MEMTIER_CLIENTS -t $MEMTIER_THREADS "
        "--data-size=$MEMTIER_DATA_SIZE --key-maximum=$MEMTIER_KEY_MAX"
    )
    out.append("flow:")
    out.append("  - name: Warm up (populate all keys)")
    out.append("    container: memtier_container")
    out.append("    commands:")
    out.append("      - type: console")
    out.append("        note: MEMTIER WARMUP")
    out.append(
        f"        command: . {env} && {common} --ratio=1:0 --key-pattern=P:P "
        "--key-minimum=1 --requests=allkeys --hide-histogram"
    )
    out.append("        shell: sh")
    out.append("        log-stdout: true")
    out.append("  - name: Run mixed GET-SET workload")
    out.append("    container: memtier_container")
    out.append("    commands:")
    out.append("      - type: console")
    out.append("        note: MEMTIER RUN")
    out.append(
        f"        command: . {env} && {common} --ratio=$MEMTIER_RATIO "
        f"--key-pattern=R:R --test-time=$MEMTIER_TEST_TIME --hide-histogram "
        f"2>&1 | tee /dev/stderr | {awk}"
    )
    out.append("        shell: sh")
    out.append("        log-stdout: true")
    out.append("        read-sci-stdout: true")
    out.append("")
    return "\n".join(out)


# PostgreSQL-only pgbench benchmarks: index a key *inside* a JSONB value and
# measure indexed equality lookups. Two variants differ only in the table they
# run against: a bespoke document table, vs. the very `usertable(YCSB_KEY,
# YCSB_VALUE jsonb)` schema the YCSB postgrenosql binding uses to model Postgres
# as a key-value store. Both live in benchmarks/pgjsonb/.
PGJSONB_VARIANTS = {
    "pg": {
        "title": "PostgreSQL JSONB query",
        "desc": ("Indexes a key inside a JSONB document and benchmarks indexed "
                 "equality lookups with pgbench"),
        "db": "jsonbbench",
        "table": "docs",
        "value_col": "doc",
        "ddl": "id bigint PRIMARY KEY, doc jsonb NOT NULL",
        "insert": ("INSERT INTO docs SELECT g, jsonb_build_object("
                   "'field0','key'||g,'category',(g%1000),'payload',md5(random()::text)) "
                   "FROM generate_series(0, $((PGJSONB_ROWS-1))) g;"),
        "index_name": "docs_field0_idx",
        "query_file": "query.sql",
    },
    "pgkv": {
        "title": "PostgreSQL KV-store JSONB query",
        "desc": ("Indexes a key inside the value column of the PostgreSQL key-value store "
                 "table (usertable/YCSB_VALUE) and benchmarks indexed equality lookups with "
                 "pgbench"),
        "db": "kvbench",
        "table": "usertable",
        "value_col": "YCSB_VALUE",
        "ddl": "YCSB_KEY VARCHAR(255) PRIMARY KEY, YCSB_VALUE JSONB NOT NULL",
        "insert": ("INSERT INTO usertable SELECT 'user'||g, jsonb_build_object("
                   "'field0','key'||g,'field1',md5(random()::text),'field2',md5(random()::text)) "
                   "FROM generate_series(0, $((PGJSONB_ROWS-1))) g;"),
        "index_name": "usertable_field0_idx",
        "query_file": "query_kv.sql",
    },
}


def gen_pgjsonb(variant: str, tier: int) -> str:
    """PostgreSQL-only benchmark: index a key *inside* a JSONB value and measure
    indexed equality lookups with pgbench (bundled in the postgres image).

    This is deliberately not part of the cross-store YCSB comparison: it exercises
    a secondary-attribute query pattern that pure KV stores cannot express and
    that YCSB's key-only postgrenosql binding does not issue. pgbench runs inside
    postgres_container (the image ships it); see TUNING.md for that trade-off.
    """
    v = PGJSONB_VARIANTS[variant]
    db, table, vcol = v["db"], v["table"], v["value_col"]
    env = "/tmp/repo/db/pg/pgjsonb/pg_pgjsonb.env"
    query = f"/tmp/repo/db/pg/pgjsonb/{v['query_file']}"
    conn = "postgresql://postgres:postgres@localhost:5432"
    suffix = " (T1 envelope-sized)" if tier else ""
    out = []
    out.append("---")
    out.append(f"name: {v['title']} benchmark{suffix}")
    out.append("author: Didi Hoffmann <didi@green-coding.io)")
    if tier:
        out.append("description: >")
        out.append(f"  {v['title']} on PostgreSQL, tuning tier T1 (envelope-sized engine")
        out.append("  config for the 4-CPU / 8-GB container). See TUNING.md.")
    else:
        out.append(f"description: {v['desc']}")
    out.append("")
    out.append(AUTOGEN)
    out.append("")
    out.append("compose-file: !include compose.yml")
    out.append("")
    out.append("custom_metrics:")
    out.append("  jsonb_queries: # functional unit: indexed JSONB lookups completed in the measured run")
    out.append("    unit: JSONB query")
    out.append("    sci: true")
    out.append("")
    out.append("services:")
    if tier:
        out.append("  postgres:")
        out.append("    command:")
        for arg in T1_COMMAND["pg"]:
            out.append(f'      - "{arg}"')
    # No load driver: the flow runs inside postgres_container (psql + pgbench),
    # so every other store service and both drivers are removed.
    out.append(disabled_block("postgres", None))
    out.append("")
    if tier:
        out.append(f"# Same benchmark flow as the default (T0) scenario ({variant}.yml).")
        out.append(f"flow: !include ['{variant}.yml', 'flow']")
        out.append("")
        return "\n".join(out)

    load = (
        f". {env} && "
        "until pg_isready -h localhost -U postgres >/dev/null 2>&1; do sleep 1; done; "
        f'psql "{conn}/postgres" -tc '
        f"\"SELECT 1 FROM pg_database WHERE datname='{db}'\" | grep -q 1 || "
        f'psql "{conn}/postgres" -c "CREATE DATABASE {db}"; '
        f'psql "{conn}/{db}" -v ON_ERROR_STOP=1 '
        f'-c "CREATE TABLE IF NOT EXISTS {table} ({v["ddl"]});" '
        f'-c "TRUNCATE {table};" '
        f'-c "{v["insert"]}"'
    )
    index = (
        f'psql "{conn}/{db}" -v ON_ERROR_STOP=1 '
        f"-c \"CREATE INDEX IF NOT EXISTS {v['index_name']} ON {table} (({vcol} ->> 'field0'));\" "
        f'-c "ANALYZE {table};"'
    )
    awk = (
        r"""awk '/transactions actually processed/{n=$NF} """
        r"""END{if(n!=""){"date +%s"|getline ts;"""
        r"""printf("%s000000 jsonb_queries=%s\n",ts,n)}}'"""
    )
    bench = (
        f". {env} && pgbench -n -c $PGJSONB_CLIENTS -j $PGJSONB_JOBS "
        f"-T $PGJSONB_DURATION -D maxid=$((PGJSONB_ROWS-1)) -f {query} "
        f'"{conn}/{db}" 2>&1 | tee /dev/stderr | {awk}'
    )

    out.append("flow:")
    out.append("  - name: Create schema and load JSONB documents")
    out.append("    container: postgres_container")
    out.append("    commands:")
    out.append("      - type: console")
    out.append("        note: LOAD JSONB DOCS")
    out.append(f"        command: {load}")
    out.append("        shell: sh")
    out.append("        log-stdout: true")
    out.append("  - name: Create index on inner JSONB key")
    out.append("    container: postgres_container")
    out.append("    commands:")
    out.append("      - type: console")
    out.append("        note: CREATE JSONB INDEX")
    out.append(f"        command: {index}")
    out.append("        shell: sh")
    out.append("        log-stdout: true")
    out.append("  - name: Run indexed JSONB query benchmark")
    out.append("    container: postgres_container")
    out.append("    commands:")
    out.append("      - type: console")
    out.append("        note: PGBENCH JSONB QUERY")
    out.append(f"        command: {bench}")
    out.append("        shell: sh")
    out.append("        log-stdout: true")
    out.append("        read-sci-stdout: true")
    out.append("")
    return "\n".join(out)


# GMT restricts scenario/flow-step `name:` values to this character set.
NAME_RE = re.compile(r"^[\.\s0-9a-zA-Z_\(\)-]+$")


def assert_valid_names(rel: Path, content: str) -> None:
    """Fail loudly if any generated `name:` would be rejected by GMT's schema."""
    for line in content.splitlines():
        stripped = line.lstrip("- ").rstrip()
        if stripped.startswith("name:"):
            value = stripped[len("name:"):].strip()
            if value and not NAME_RE.match(value):
                raise SystemExit(
                    f"{rel}: name {value!r} has characters GMT rejects "
                    r"(allowed: . _ ( ) - space alnum)"
                )


def targets() -> dict[Path, str]:
    """Map every generated file path -> its content."""
    files: dict[Path, str] = {}
    root_compose = (REPO / "compose.yml").read_text()
    for key in STORES:
        for tier in (0, 1):
            base = f"{key}.yml" if tier == 0 else f"{key}.t1.yml"
            files[REPO / "benchmarks" / "ycsb" / base] = gen_ycsb(key, tier)
            if STORES[key]["resp"]:  # memtier has no PostgreSQL (no RESP/memcache protocol)
                files[REPO / "benchmarks" / "memtier" / base] = gen_memtier(key, tier)
    # PostgreSQL-only JSONB query benchmarks (no cross-store analog): a bespoke
    # document table (pg) and the KV-store usertable schema (pgkv).
    for variant in PGJSONB_VARIANTS:
        for tier in (0, 1):
            base = f"{variant}.yml" if tier == 0 else f"{variant}.t1.yml"
            files[REPO / "benchmarks" / "pgjsonb" / base] = gen_pgjsonb(variant, tier)
    for path, content in files.items():
        assert_valid_names(path.relative_to(REPO), content)
    # Each benchmark dir keeps its own copy of compose.yml (GMT !include can't
    # escape the scenario directory).
    for bench_dir in ("ycsb", "memtier", "pgjsonb"):
        files[REPO / "benchmarks" / bench_dir / "compose.yml"] = root_compose
    return files


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--check", action="store_true", help="fail if committed files are stale")
    args = ap.parse_args()

    stale = []
    for path, content in targets().items():
        if not content.endswith("\n"):
            content += "\n"
        current = path.read_text() if path.exists() else None
        if current == content:
            continue
        stale.append(path.relative_to(REPO))
        if not args.check:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(content)

    if args.check:
        if stale:
            print("stale generated files (run ./gen_scenarios.py):", file=sys.stderr)
            for p in stale:
                print(f"  {p}", file=sys.stderr)
            return 1
        print("generated scenarios are up to date")
        return 0

    if stale:
        print(f"wrote {len(stale)} file(s)")
        for p in stale:
            print(f"  {p}")
    else:
        print("nothing to do (already up to date)")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
